// The published @carbon/icons 11.83.0 only ships one file per icon (usually just 32.js or
// a size-agnostic "glyph" index.js) even though @carbon/web-components imports specific
// sizes like chevron--right/16.js. The esbuild plugin in build-wc-bundle.mjs was falling
// back to whatever file existed, which for most icons is the WRONG artwork — e.g.
// chevron--right at 16px is a purpose-drawn glyph, not the generic auto-scaling one used
// as a stand-in, so icons visibly render at the wrong proportions.
//
// @carbon/icons/metadata.json (also shipped in the package, just not wired into the "es/"
// exports) contains the exact same {elem, attrs, content, name, size} descriptor that each
// real per-size module is supposed to export, for every size of every icon. This script
// reads that descriptor straight from metadata.json for the exact (icon, size) pairs
// @carbon/web-components actually imports and writes the real module — same format, same
// data, just filled in from the one place in the package where it still exists complete.
import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from 'node:fs';

const metadata = JSON.parse(readFileSync('node_modules/@carbon/icons/metadata.json', 'utf8'));
const byName = new Map(metadata.icons.map((i) => [i.name, i]));

// Every `@carbon/icons/es/<name>/<size>.js` import actually used by @carbon/web-components
// — scanned fresh each run (not hand-maintained) so this stays correct as the package
// evolves. Saved alongside for inspection, but not required to exist beforehand.
function findIconImports(dir) {
  const found = new Set();
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = `${dir}/${entry.name}`;
    if (entry.isDirectory()) {
      for (const m of findIconImports(full)) found.add(m);
    } else if (entry.name.endsWith('.js')) {
      const src = readFileSync(full, 'utf8');
      for (const m of src.matchAll(/@carbon\/icons\/es\/([^'"]+\/(?:16|20|24|32)\.js)/g)) found.add(m[1]);
    }
  }
  return found;
}
const imports = [...findIconImports('node_modules/@carbon/web-components/es/components')].sort();
writeFileSync('scripts/wc-icon-imports.txt', imports.join('\n') + '\n');

let written = 0;
const skipped = [];

for (const line of imports) {
  const [kebabName, sizeFile] = line.split('/');
  const requestedSize = Number(sizeFile.replace(/\.js$/, ''));
  const existingPath = `node_modules/@carbon/icons/es/${kebabName}/${sizeFile}`;
  if (existsSync(existingPath)) continue; // already correct, nothing to override

  const icon = byName.get(kebabName);
  const outputEntry = icon?.output?.find((o) => o.size === requestedSize);
  if (!outputEntry) { skipped.push(line); continue; }

  const dir = `scripts/icon-overrides/${kebabName}`;
  mkdirSync(dir, { recursive: true });
  writeFileSync(
    `${dir}/${sizeFile}`,
    `// Generated by scripts/build-icon-overrides.mjs from @carbon/icons/metadata.json's\n` +
      `// real ${requestedSize}px descriptor for "${kebabName}" — @carbon/icons 11.83.0's own\n` +
      `// "es/" build doesn't ship this size, even though the source data still has it.\n` +
      `const data = ${JSON.stringify(outputEntry.descriptor, null, 2)};\nexport default data;\n`
  );
  written++;
}

console.log(`wrote ${written} icon override(s) / ${imports.length} total imports`);
if (skipped.length) console.log('still missing (no metadata for this size either):', skipped.join(', '));
